#Corrige el siguiente condicional if else

color = "rojo"


if color != "rojo": #Abrimos ciclo if donde especificamos si color es DIFERENTE DE "rojo" , se cumple el ciclo
    print("El color no es rojo.") #Se imprime el mensaje si se cumplieron las condiciones anteriores

else:         #Usamos else, en dado caso que la condición anterior no se cumpla por lo que se ejecutara esta instrucción SI o SI
    print ("El color es rojo.") #Imprime el mensaje si no se cumplió la primera condición
